    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException { 
               
    }

    private void deleteAccount(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException, SQLException {
        String id = request.getParameter("adminId");
        EmployeeDAO dao = new EmployeeDAO();
        dao.deleteUser(id);
        response.sendRedirect("delete?action=delete-account");
    }
    
